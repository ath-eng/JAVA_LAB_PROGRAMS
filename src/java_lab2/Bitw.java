import java.util.*;
public class Bitw 
{
 public static void main(String args[])
 {
  int x=10,y=5;
  System.out.println("x="+ x);
  System.out.println("y="+ y);
  int z=~x;
  System.out.println("z="+ z);
  System.out.println("x="+ (x<<1));
  System.out.println("y="+ (y>>1));
 }   
}
