import java.util.*;
public class Incde
{
 public static void main(String arg[])
 {
     int a=10,b=5;
     System.out.println("a="+ a);
     System.out.println("b="+ b);
     System.out.println("a="+ ++a);
     System.out.println("b="+ b++);
     System.out.println("a="+ a);
     System.out.println("b="+ b);
     System.out.println("a="+ --a);
     System.out.println("a="+ a);
     System.out.println("b="+ b--);
     System.out.println("b="+ b);
 }
 }   
       
    
